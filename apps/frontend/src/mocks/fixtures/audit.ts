function buildCallLogs(): CallLog[] {
  return CALL_LOG_TEMPLATES.map((tpl, i) => {
    const day = (i % 19) + 1
    const hour = 8 + (i % 12)
    const minute = (i * 7) % 60
    const second = (i * 13) % 60
    const inputTokens = 600 + (i % 8) * 400
    const outputTokens = tpl.status === 'error' ? 0 : 280 + (i % 6) * 200
    return {
      id: `call-${i + 1}`,
      ...tpl,
      inputTokens,
      outputTokens,
      latencyMs: 800 + (i % 10) * 450,
      cost:
        tpl.status === 'filtered'
          ? 0
          : Math.round((inputTokens * 0.003 + outputTokens * 0.01) * 100) / 100,
      createdAt: `2026-06-${String(day).padStart(2, '0')} ${String(hour).padStart(2, '0')}:${String(minute).padStart(2, '0')}:${String(second).padStart(2, '0')}`,
      inputPreview: INPUT_PREVIEWS[i % INPUT_PREVIEWS.length],
      outputPreview: OUTPUT_PREVIEWS[i % OUTPUT_PREVIEWS.length],
    }
  })
}

